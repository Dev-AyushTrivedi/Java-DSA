public class Numberfrom1to10 {
    public static void main(String[] args) {
        int counter = 1;
        while(counter <= 10){
            System.out.print(counter+" "); //for printing in same line with space use only print along with " "
            System.out.println(counter);  //for printing in multiple line with space use only println
            counter++;
        }
    }
}
