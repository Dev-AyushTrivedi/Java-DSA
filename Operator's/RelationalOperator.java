public class RelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println(a == b); // == checks equal or not
        System.out.println(a != b); // != checks the operands are not equal
        System.out.println(a > b);  // > checks the first operand is greater than the second
        System.out.println(a < b); // < checks the first operand is less than the second
        System.out.println(a >= b); // >= checks the first operand is greater than or equal to second
        System.out.println(a <= b); // <= checks the first operand is less than or equal to second
    }
}
